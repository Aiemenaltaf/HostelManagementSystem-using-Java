    private void SearchDutyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SearchDutyKeyReleased
        // TODO add your handling code here:
         DefaultTableModel model= (DefaultTableModel) DutyTable.getModel();
        String search= SearchDuty.getText();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        DutyTable.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(search));
    }//GEN-LAST:event_SearchDutyKeyReleased

