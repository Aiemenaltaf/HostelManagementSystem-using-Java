    private void UpdateDutyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateDutyActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model= (DefaultTableModel) DutyTable.getModel();
        if(DutyTable.getSelectedRowCount()==1){
        String Duty_Name = D_Name.getText();
        String Salary = salary.getText();
        
         DutyTable.setValueAt(Duty_Name, DutyTable.getSelectedRow(), 0);
        DutyTable.setValueAt(Salary, DutyTable.getSelectedRow(), 1);
        
        JOptionPane.showMessageDialog(null,"Duty is Updated Successfully");
        }
        else{
        if(DutyTable.getRowCount()==0){
            JOptionPane.showMessageDialog(null,"Table is empty");
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select any Duty!");
        }
        }      
        
    }//GEN-LAST:event_UpdateDutyActionPerformed

