         public ArrayList DutyNameList(String Duty_Name, String Salary)
    {
        ArrayList<DutyName> list = new ArrayList <DutyName>();
        DutyName D = new DutyName(Duty_Name,Salary);
        list.add(D);
        return list;
        
    }
    
    public void addRow(String Duty_Name, String Salary){
        
        DefaultTableModel model= (DefaultTableModel) DutyTable.getModel();
        ArrayList<DutyName> list = DutyNameList(Duty_Name,Salary);
        Object rowData[] = new Object[2];
        for(int i = 0 ;i<list.size();i++){
        rowData[0] = list.get(i).getDutyName();
        rowData[1] = list.get(i).getSalary();
        model.addRow(rowData);
    }
    }

